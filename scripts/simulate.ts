// ── 実行 ─────────────────────────────────────────────────────────

const RARITY_LABEL: Record<string, string> = { RARE: '★★★', MIDDLE: '★★ ', STANDARD: '★  ' }

let passed = 0
let failed = 0

console.log('\n=== ジョブ診断シミュレーション ===\n')
console.log('期待ジョブ  → 結果ジョブ  [獲得スキル TOP6]')
console.log('─'.repeat(70))

for (const [expected, answers] of Object.entries(personas)) {
  const result = diagnose(answers)
  const ok = result.job === expected
  const icon = ok ? '✅' : '❌'
  const rarity = RARITY_LABEL[result.rarity]

  if (ok) passed++
  else failed++

  const topSkillsStr = [...result.topSkills].join(', ')
  const jobMatch = ok
    ? `${result.job}`
    : `${result.job}  ← 期待: ${expected}`

  console.log(`${icon} ${rarity} ${expected.padEnd(8)}→ ${jobMatch}`)
  console.log(`         スキル: [${topSkillsStr}]`)

  if (!ok) {
    // スコア詳細を表示して原因を把握しやすくする
    const sorted = Object.entries(result.skillScores).sort((a, b) => b[1] - a[1])
    console.log(`         スコア: ${sorted.map(([s, n]) => `${s}:${n}`).join(' / ')}`)
  }
  console.log()
}

console.log('─'.repeat(70))
console.log(`結果: ${passed}/${passed + failed} 件一致`)
if (failed > 0) {
  console.log('\n❌ の項目はジョブ定義またはスコアリングの調整を検討してください。')
}
